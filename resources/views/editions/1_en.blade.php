<section aria-labelledby="about-3" class="about-block" aria-live="polite">
    <h3 id="about-3">Who can participate?</h3>

    <ul>
        <li><strong>Neighbors who are 16 or older</strong> and are <strong>registered in la Vila Joiosa</strong>.</li>
        <li><strong>Entities and/or associations</strong> in the municipal registry.</li>
        <li>Up to <strong>3 proposals</strong> may be submitted per person and/or entity.</li>
    </ul>
</section>

<section aria-labelledby="about-4" class="about-block" aria-live="polite">
    <h3 id="about-4">Criteria for the proposals</h3>

    <ul>
        <li>The proposals must be within the scope of municipal jurisdiction.</li>
        <li>They must be specific, economically mesurable and they must respond to a specific need.</li>
        <li>They must comply with current regulations.</li>
        <li>Related to general interest (not responding to particular interests).</li>
        <li>Technically feasible.</li>
        <li>They must not compromise the budgets of other years.</li>
        <li>They must be submitted within the established deadline.</li>
        <li>They cannot be actions that are already being carried out or are planned in the municipal budget.</li>
        <li>Respectful of the criteria of social inclusion, sustainability, Human Rights, equity and religious freedom.</li>
        <li>They cannot state who will execute them.</li>
    </ul>
</section>

<section aria-labelledby="about-5" class="about-block" aria-live="polite">
    <h3 id="about-5">How will the proposals be evaluated?</h3>

    <p>
        The municipal technicians will be responsible for assessing the proposals
        and preparing the corresponding reports. Faced with two or more similar proposals,
        the municipal technical services will assess whether they can be unified.
    </p>
</section>

<section aria-labelledby="about-6" class="about-block" aria-live="polite">
    <h3 id="about-6">How will voting take place?</h3>

    <p>
        Once all the proposals received that meet the requirements have been assessed,
        they will finally advance to the voting phase. All persons over the age of 16 
        registered in la Vila Joiosa will be able to vote. The most voted proposals
        will be selected until the budget is exhausted.
    </p>
</section>

<section aria-labelledby="about-7" class="about-block" aria-live="polite">
    <h3 id="about-7">How and when will the vote take place?</h3>

    <p>
        Once the finalist proposals have been defined, a voting period will start,
        in which votes can be cast electronically on this website.
    </p>
</section>

<section aria-labelledby="about-7" class="about-block" aria-live="polite">
    <h3 id="about-7">¿Cómo se darán a conocer los resultados?</h3>

    <p>
        The results of the vote will be published in the different media of the City Council and, then,
        the procedures will begin to execute the selected projects.
    </p>
</section>
