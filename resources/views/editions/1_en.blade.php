<section aria-labelledby="about-1" class="about-block" aria-live="polite">
    <h3 id="about-1">La Vila Participa</h3>
    <img src="/images/choice-2.svg" class="about-block__thumbnail" alt="" aria-hidden="true" />

    <p>
      The <strong>Town Council of la Vila Joiosa</strong> believes in democratic regeneration
      as one of its fundamental pillars. A regeneration that entails giving citizens
      a voice and bringing them closer to the decision-making process. A participation
      that aims to give the citizens a bigger share of political co-responsability,
      so they can <strong>“co-govern” their council, their town.</strong>
    </p>
    <p>
      It started two years ago with this format of participatory budgeting and for this second edition, the process will be more dynamic, broader in reach, and easier to take a part in.
    </p>
    <p>
      <strong>La Vila Participa</strong> aims to delve into the real and effective participation of its citizens.
    </p>
    <p>
      To this end, the Government of la Vila Joiosa has decided that the <strong>ten projects</strong>
      that make it to the ballot will be chosen from among citizen submissions.
      On <strong>February 2020, a four-week proposal phase will start so that citizens
      may submit their proposals for local projects. The proposals will then be assessed and
      analysed by municipal technicians, who will assign an estimated budget, determine
      who would benefit from the project, or provide a technical reason should the proposal be rejected.
    </p>
    <p>
      Afterwards, the citizens will choose up to <strong>three projects</strong>
      that they prefer to be carried out. The information regarding the proposals
      will be available to consult on this website, the polling stations, as well as
      other different media. A public presentation event of the ten projects will
      also be held.
    </p>
    <p>
      The citizens or associations that have put forward a proposal chosen for the
      final ballot will have the chance to present it publicly in an open session
      organised by the Council, which will be in addition to any other form of
      promotion that they may carry out on their own if they wish.
    </p>
</section>

