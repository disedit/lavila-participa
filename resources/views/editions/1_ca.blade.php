<section aria-labelledby="about-3" class="about-block" aria-live="polite">
    <h3 id="about-3">Qui hi pot participar?</h3>

    <ul>
        <li><strong>Veïns i veïnes</strong> a títol individual <strong>majors de 16 anys</strong> i que estiguen <strong>empadronades a la Vila Joiosa</strong>.</li>
        <li><strong>Entitats i/o associacions</strong> inscrites al registre municipal.</li>
        <li>Es podrà presentar un màxim de <strong>3 propostes</strong> per persona i/o entitat.</li>
    </ul>
</section>

<section aria-labelledby="about-4" class="about-block" aria-live="polite">
    <h3 id="about-4">Criteris per a les propostes</h3>

    <ul>
        <li>Les propostes han de ser dins de l’àmbit d’inversions de competència municipal.</li>
        <li>Concretes, valorables econòmicament i que donen una resposta a una necessitat determinada.</li>
        <li>Que complisquen la normativa vigent.</li>
        <li>Relacionades amb un interés general (no respondre a interessos particulars).</li>
        <li>Viables tècnicament.</li>
        <li>Que no comprometen els pressupostos d’altres anys.</li>
        <li>S’hauran de presentar dins el termini establert que es marque.</li>
        <li>No poden ser actuacions que ja s’estiguen portant a terme o que estiguen previstes al pressupost municipal.</li>
        <li>Respectuoses amb els criteris d’inclusió social, sostenibilitat, Drets Humans, equitat i llibertat religiosa.</li>
        <li>No poden comprometre qui les executarà.</li>
    </ul>
</section>

<section aria-labelledby="about-5" class="about-block" aria-live="polite">
    <h3 id="about-5">Com es valoraran les propostes?</h3>

    <p>
        Els tècnics municipals seran els responsables de valorar les propostes i realitzar els informes corresponents.
        Davant de dues o més propostes similars, els serveis tècnics municipals valoraran si es poden unificar.
    </p>
</section>

<section aria-labelledby="about-6" class="about-block" aria-live="polite">
    <h3 id="about-6">Com es faran les votacions?</h3>

    <p>
        Una vegada valorades totes les propostes rebudes que complisquen els requisits, passaran, finalment, a la fase de votació.
        Podran votar totes les persones majors de 16 anys empadronades a la Vila Joiosa.
        S’aniran seleccionant les propostes més votades fins que s'esgote el pressupost.
    </p>
</section>

<section aria-labelledby="about-7" class="about-block" aria-live="polite">
    <h3 id="about-7">Com i quan es realitzarà la votació?</h3>

    <p>
        Una vegada definides les propostes finalistes, s’obrirà un període de votació,
        que es podrà fer telemàticament a través d'aquesta pàgina web.
    </p>
</section>

<section aria-labelledby="about-7" class="about-block" aria-live="polite">
    <h3 id="about-7">Com es donaran a conéixer els resultats?</h3>

    <p>
        Els resultats de la votació es publicaran als diferents mitjans de l’Ajuntament i, aleshores,
        s’iniciaran els tràmits per executar les actuacions que hagen resultat guanyadores.
    </p>
</section>
